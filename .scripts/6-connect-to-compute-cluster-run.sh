# TEST
## Checkout
git checkout $(git log main --pretty=oneline | grep "admin/connect-to-compute-cluster/0008" | cut -c1-40)
## Run test case
./.scripts/6-connect-to-compute-cluster-test/1-test-cat1.sh

# CMD
## Checkout
git checkout $(git log main --pretty=oneline | grep "admin/connect-to-compute-cluster/0011" | cut -c1-40)
## Run command
